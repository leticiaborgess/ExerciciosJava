package exercicios;
/*A jornada de trabalho semanal de um funcionário é de 40 horas. 
 * O funcionário que trabalhar mais de 40 horas receberá hora extra, 
 * cujo cálculo é o valor da hora regular com um acréscimo de 50%. 
 * Escreva um algoritmo que leia o número de horas trabalhadas em um mês,
 * o salário por hora e escreva o salário total do funcionário, 
 * que deverá ser acrescido das horas extras, caso tenham sido trabalhadas
 * (considere que o mês possua 4 semanas exatas)
 * */

import java.util.Scanner;

public class SalarioIfElse {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);

		int horas;
		double salarioHora;

		System.out.println("Horas Trabalhadas no mês: ");
		horas = sc.nextInt();

		System.out.println("Salário por hora: ");
		salarioHora = sc.nextDouble();

		double custo = horas * salarioHora;
		double salarioBruto = 160 * salarioHora;
		double horaExtra = ((horas - 160) * (salarioHora * 1.50));

		if (horas <= 160) {
			System.out.println("Custo: R$" + custo);

		} else if (horas > 160) {
			System.out.println("Custo: R$" + (salarioBruto + horaExtra));

		}
		sc.close();
	}

}
