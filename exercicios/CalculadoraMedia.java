package exercicios;

public class CalculadoraMedia {
	
	/*Criar 4 variáveis com o nome nota1, nota2, nota3 e nota 4 
	 * com valores iniciais qualquer e exibir a média no console
	 */

	public static void main(String[] args) {
		double nota1 = 10;
		double nota2 = 9.4;
		double nota3 = 6;
		double nota4 = 4.6;
		double media = (nota1 + nota2 + nota3 + nota4)/4f;
		
		System.out.println("A média é: " + media);
		

	}

}
